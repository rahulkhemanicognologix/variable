package varialble.learning;

public class WhileLoop {
    public static void main(String[] args) {


        int r = 2;
        while (r <= 20) {
            System.out.println(r);
            r++;
        }
        System.out.println();
        int a = 2;
        while(a<=12){
            System.out.println(a);
            a++;
        }
        System.out.println();

        int x=1;
        int add=0;
        while(x<=5){
            add=add+x;
            x++;
            System.out.println();

        }
        System.out.println(add);
    }
}
