package varialble.learning;

public class LogicalOperators {
    public static void main(String[] args) {
        int a = 5;
        int b = 4;
        int c = 10;
        int d = 112;
        System.out.println( a>b | b>++a);
        System.out.println(a);

        System.out.println(d>c||++c>d);
        System.out.println(c);

    }
}
